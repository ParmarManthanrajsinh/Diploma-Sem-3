# Create  a  user  defined  module  with  simple  functions  for:  addition,  subtraction, 
# multiplication, division, modulo,  square,  factorial.  Write  a  program to  import  the 
# module and access functions defined in the module.

import maths

print("3 + 4 = ",maths.addition(3,4))
print("3 - 4 = ",maths.subtraction(3,4))
print("3 x 4 = ",maths.multiplication(3,4))
print("3 % 4 = ",maths.modulo(3,4))
print("3^4 = ",maths.square(3,4))
print("!3 = ",maths.factorial(3))

